package entity.advert;

import org.hibernate.validator.constraints.NotEmpty;

public class AdvertEntity {
	private int id;
	private String advertUrl;//广告图片路径
	@NotEmpty
	private String descride;//内容
	private String goodsCode;//商品编号
	private int advertClick;//广告点击次数
	private int advertShow;//是否展示的状态
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getDescride() {
		return descride;
	}
	public void setDescride(String descride) {
		this.descride = descride;
	}
	public String getGoodsCode() {
		return goodsCode;
	}
	public void setGoodsCode(String goodsCode) {
		this.goodsCode = goodsCode;
	}
	public String getAdvertUrl() {
		return advertUrl;
	}
	public void setAdvertUrl(String advertUrl) {
		this.advertUrl = advertUrl;
	}
	public int getAdvertClick() {
		return advertClick;
	}
	public void setAdvertClick(int advertClick) {
		this.advertClick = advertClick;
	}
	public int getAdvertShow() {
		return advertShow;
	}
	public void setAdvertShow(int advertShow) {
		this.advertShow = advertShow;
	}
	
}
