package entity.csrRecord;

import java.util.Date;

/**
 * 客服记录实体类
 * 客服为客户服务的记录
 * @author :lyy
 * @date :上午9:50:11 2019年5月9日
 *
 */
public class CsrRecordEntity {
	
	private int id;//记录id
	private String userCode;//用户编码
	private String csrCode;//客服编码
	private String csrName;//客服名字
	private String userName;//用户名字
	private Date startTade;//开始时间（与用户开始聊天时间）
	private Date endTade;//结束时间(与用户聊天结束时间)
	private Date totalTade;//接待时长
	private String recordCsat;//满意度
	private String recordContent;//具体内容
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getUserCode() {
		return userCode;
	}
	public void setUserCode(String userCode) {
		this.userCode = userCode;
	}
	public String getCsrCode() {
		return csrCode;
	}
	public void setCsrCode(String csrCode) {
		this.csrCode = csrCode;
	}
	public String getCsrName() {
		return csrName;
	}
	public void setCsrName(String csrName) {
		this.csrName = csrName;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public Date getStartTade() {
		return startTade;
	}
	public void setStartTade(Date startTade) {
		this.startTade = startTade;
	}
	public Date getEndTade() {
		return endTade;
	}
	public void setEndTade(Date endTade) {
		this.endTade = endTade;
	}
	public Date getTotalTade() {
		return totalTade;
	}
	public void setTotalTade(Date totalTade) {
		this.totalTade = totalTade;
	}
	public String getRecordCsat() {
		return recordCsat;
	}
	public void setRecordCsat(String recordCsat) {
		this.recordCsat = recordCsat;
	}
	public String getRecordContent() {
		return recordContent;
	}
	public void setRecordContent(String recordContent) {
		this.recordContent = recordContent;
	}
	@Override
	public String toString() {
		return "CsrRecord [id=" + id + ", userCode=" + userCode + ", csrCode=" + csrCode + ", csrName=" + csrName
				+ ", userName=" + userName + ", startTade=" + startTade + ", endTade=" + endTade + ", totalTade="
				+ totalTade + ", recordCsat=" + recordCsat + ", recordContent=" + recordContent + "]";
	}
}
