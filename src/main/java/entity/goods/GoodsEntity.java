package entity.goods;

public class GoodsEntity {
	private int id;
	private String goodsName;//商品名字
	private int goodsPrice;//商品价格
	private String goodsPath;//商品图片
	private String goodsDescribe;//商品描述
	private String goodsColor;//商品颜色
	private String typeCode;//商品类型编号
	private String agentCode;//代理商编号
	private String code;
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getGoodsName() {
		return goodsName;
	}
	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}
	public int getGoodsPrice() {
		return goodsPrice;
	}
	public void setGoodsPrice(int goodsPrice) {
		this.goodsPrice = goodsPrice;
	}
	public String getGoodsPath() {
		return goodsPath;
	}
	public void setGoodsPath(String goodsPath) {
		this.goodsPath = goodsPath;
	}
	public String getGoodsDescribe() {
		return goodsDescribe;
	}
	public void setGoodsDescribe(String goodsDescribe) {
		this.goodsDescribe = goodsDescribe;
	}
	public String getGoodsColor() {
		return goodsColor;
	}
	public void setGoodsColor(String goodsColor) {
		this.goodsColor = goodsColor;
	}
	public String getTypeCode() {
		return typeCode;
	}
	public void setTypeCode(String typeCode) {
		this.typeCode = typeCode;
	}
	public String getAgentCode() {
		return agentCode;
	}
	public void setAgentCode(String agentCode) {
		this.agentCode = agentCode;
	}
	@Override
	public String toString() {
		return "GoodsEntity [id=" + id + ", goodsName=" + goodsName + ", goodsPrice=" + goodsPrice + ", goodsPath="
				+ goodsPath + ", goodsDescribe=" + goodsDescribe + ", goodsColor=" + goodsColor + ", typeCode="
				+ typeCode + ", agentCode=" + agentCode + ", code=" + code + "]";
	}
	
	
}
